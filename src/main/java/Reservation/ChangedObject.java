package Reservation;

class ChangedObject {

    private Changed changed;
    private Object arg;

    public Object getArg() {
        return arg;
    }

    public void setArg(Object arg) {
        this.arg = arg;
    }

    public Changed getChanged() {
        return changed;
    }

    public void setChanged(Changed changed) {
        this.changed = changed;
    }
}
